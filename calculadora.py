n1 = input("Ingresa el primer numero")
n2 = input("Ingresa el segundo numero")

n1 = int(n1)
n2 = int(n2)

suma = n1 + n2
resta = n1 - n2
multiplicacion = n1 * n2
division = n1 / n2

print("Para estos numeros el resultado de la suma es {}\n"
      "El resultado de la resta es {}\n"
      "El resultado de la multiplicacion es {}\n"
      "El resultado de la division es {}".format(suma, resta, multiplicacion, division))